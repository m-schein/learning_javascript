//funções anonimas(arrow function)
const funcao = (param1, param2, param3) => {
  console.log(param1, param2, param3);
  return param1 + param2 + param3;
}
funcao(1,2,3)

//funções normais 
function funcao2 (param1, param2, param3) {
  console.log(param1, param2, param3);
  return param1 + param2 + param3;
}
funcao2(3,3,3)